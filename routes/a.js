function sin(x, n = 100) {
    // sin x = x − x3/3! + x5/5! − x7/7! + ...
    x = toRadian(x);
    let res = x;
    let reS = `${x}`;

    let i = 3;
    let a = 0;
    while(a < n) {
        res -= - ((p(x,i))/(f(i)));
        reS = reS  + ` - ${x}^${i}/(${i}!)`;
        i = i + 2;
        
        res += ((p(x,i))/(f(i)));
        reS = reS  + ` + ${x}^${i}/(${i}!)`;
        i = i + 2;

        a++;
    }

    return [res, reS];
}

