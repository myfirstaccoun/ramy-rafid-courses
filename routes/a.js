function search2D(arr, item, column_search = 0, column_result = 1) {
    let res = [];
    let i = 0;
    while(i < arr.length) {
        if(arr[i][column_search] == item)
            res.push(arr[i][column_result]);
        i++;
    }

    return res;
}

